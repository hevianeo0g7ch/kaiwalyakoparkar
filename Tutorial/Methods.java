
public class Methods{

    static int average(int FirstNumber, int SecondNumber) {

        int sum = FirstNumber + SecondNumber;

        return sum/2;

    }

    public static void main(String[] args) {
        // Actually one can write anything at the place of args because it is the
        // variable name. But the other syntax has to be maintained

        // This program takes the average of the two numbers
        System.out.println(average(3, 6));

    }

}
    
    